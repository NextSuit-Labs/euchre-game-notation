const fs = require("fs");
const path = require("path");

const repoRoot = __dirname;
const schemaDir = path.join(repoRoot, "schemas");
const outputPath = path.join(repoRoot, "src", "proto-schemas.ts");

const schemaFiles = [
  ["COMMON_PROTO_SCHEMA", "egn-common.proto"],
  ["CONDENSED_PROTO_SCHEMA", "egn.proto"],
  ["EXPANDED_PROTO_SCHEMA", "egn-expanded.proto"],
];

function readSchema(fileName) {
  return fs.readFileSync(path.join(schemaDir, fileName), "utf8");
}

function generateProtoSchemasSource() {
  return [
    "// This file is auto-generated by generate-proto-schemas.js.",
    "// Do not edit it manually; update the .proto files in /schemas and regenerate.",
    "",
    ...schemaFiles.map(([exportName, fileName]) => `export const ${exportName} = ${JSON.stringify(readSchema(fileName))};`),
    "",
  ].join("\n");
}

function writeProtoSchemasFile() {
  fs.writeFileSync(outputPath, generateProtoSchemasSource(), "utf8");
}

if (require.main === module) {
  writeProtoSchemasFile();
}

module.exports = {
  generateProtoSchemasSource,
  outputPath,
};